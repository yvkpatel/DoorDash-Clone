# @intializes  
#           Type: Enum
#           Description: Entity type i.e. Customer, Driver, Restaurant. 
#           Type: String
#           Description: entity id
class Entity:

    def __init__(self, type, id):
        self.type = type
        self.id = id 

    def getType():
        return self.type
    
    def getId():
        return self.id